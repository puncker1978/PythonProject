# Задание 5: Деление яблок n школьников делят k яблок поровну. Напишите программу, которая определяет,
# сколько яблок достанется каждому школьнику и сколько яблок останется в корзине.
n = int(input('Количество яблок: '))
k = int(input('Количество школьников: '))
print('Каждому достанется', n // k)
print('В корзине останется', n % k)

# Задача 6: Перевод секунд в минуты и секунды
# Напишите программу, которая принимает на вход количество секунд и переводит его в минуты и секунды в формате:
# <секунды> сек - это <минуты> мин <секунды> сек
all_seconds = int(input())
minuts = all_seconds // 60
seconds = all_seconds % 60
print(all_seconds, 'сек - это', minuts, 'мин', seconds, 'сек')